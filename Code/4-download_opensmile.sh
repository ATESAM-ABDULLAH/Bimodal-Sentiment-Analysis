# Download the opensmile pre-built for linux
wget -c 'https://github.com/audeering/opensmile/releases/download/v3.0.2/opensmile-3.0.2-linux-x86_64.zip'

# Unzip the downloaded file
unzip opensmile-3.0.2-linux-x86_64.zip

# Move the downloaded zip file to a 'tar' folder for storage
mv opensmile-3.0.2-linux-x86_64.zip tar/

# Rename the unzipped folder for ease of use
mv opensmile-3.0.2-linux-x86_64 opensmile